/*
Учитывая массив целых чисел nums и целое число target, верните индексы двух чисел так,
чтобы они складывались в target. Вы можете предположить, что каждый вход будет иметь
ровно одно решение, и не можете использовать один и тот же элемент дважды.
Вы можете вернуть ответ в любом порядке.

Найти два числа в массиве, которые в сумме дают target, и вернуть их индексы.

nums = [2, 7, 11, 15], target = 9
2 + 7 = 9 → индексы [0, 1]
*/

function twoSum(nums, target) {
  const map = new Map(); // Будет хранить: число → его индекс

  for (let i = 0; i < nums.length; i++) {
    const currentNum = nums[i];
    const complement = target - currentNum; // Какое число нам нужно?

    // Проверяем, видели ли мы нужное число раньше
    if (map.has(complement)) {
      // Нашли пару! Возвращаем индексы
      return [map.get(complement), i];
    }

    // Сохраняем текущее число и его индекс
    map.set(currentNum, i);
  }
}

console.log(twoSum([2, 7, 11, 15], 9)); // [0, 1]
console.log(twoSum([3, 2, 4], 6)); // [1, 2] (2+4=6)
console.log(twoSum([3, 3], 6)); // [0, 1] (3+3=6)
console.log(twoSum([8, 1, 4, 2, 3], 5)); // [1, 2] (1+4=5)

