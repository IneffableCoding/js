const majorityElement = function (nums) {
  const hash = {}; // Объект-счетчик: {число: количество_вхождений}
  let res = 0; // Будет хранить текущий самый частый элемент
  let majority = 0; // Будет хранить максимальное количество вхождений

  for (let n of nums) {
    hash[n] = (hash[n] || 0) + 1;
    // hash[n] || 0 — если hash[n] не существует (первое встреченное число), берём 0
    // + 1 — увеличиваем счетчик для этого числа
    // hash[n] = ... — сохраняем новое значение
    if (hash[n] > majority) {
      res = n;
      majority = hash[n];
    }
    /*
    Если текущее число n встретилось больше раз, чем предыдущий максимум (majority)
    То обновляем:
    res = теперь это число n
    majority = новое максимальное количество
    */
  }

  return res;
  // После цикла в res хранится элемент с максимальной частотой
};

console.log(majorityElement([3, 2, 3]));

